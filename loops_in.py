# For loop in python
# Syntax:
# for variable in sequence:
#     # code block to be executed
# Example:
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)
# Output:
# apple
# banana
# cherry

for i in range(5):
    print(i)
# Run from 0 to 4
# Output:
# 0
# 1
# 2
# 3
# 4
for i in range(1, 6, 2):
    print(i)
# Run from 1 to 5 with a step of 2
# Output:
# 1
# 3
# 5

# While loop in python
# Syntax:
# while condition:
#     # code block to be executed

count = 0
while count < 5:
    print(count)
    count += 1
# Output:
# 0
# 1
# 2
# 3
# 4
